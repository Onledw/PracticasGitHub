/**
 * Multiplies two numbers.
 * @param {number} a - The first number.
 * @param {number} b - The second number.
 * @returns {number} The product of a and b.
 * @example
 * // Example usage:
 * multiply(4, 5);
 * // Expected output: 20
 */
function multiply(a, b) {
    return a * b;
}
